package th.co.pt.pcca.pccaservice.entities.member;

public class WorkStateCriteriaObj {
	
	private String codempid;
	private String month;
	private String year;
	private String approver;
	private String search_case;
	public String getCodempid() {
		return codempid;
	}
	public void setCodempid(String codempid) {
		this.codempid = codempid;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getApprover() {
		return approver;
	}
	public void setApprover(String approver) {
		this.approver = approver;
	}
	public String getSearch_case() {
		return search_case;
	}
	public void setSearch_case(String search_case) {
		this.search_case = search_case;
	}
	
	
}
