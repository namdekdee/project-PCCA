import java.math.BigDecimal;

public class WorkTimeLeaveObj {
	
	
	private BigDecimal leave_qty;
	private BigDecimal leaved_amt;
	private BigDecimal leave_repay;
	
	public BigDecimal getLeave_qty() {
		return leave_qty;
	}
	public void setLeave_qty(BigDecimal leave_qty) {
		this.leave_qty = leave_qty;
	}
	public BigDecimal getLeaved_amt() {
		return leaved_amt;
	}
	public void setLeaved_amt(BigDecimal leaved_amt) {
		this.leaved_amt = leaved_amt;
	}
	public BigDecimal getLeave_repay() {
		return leave_repay;
	}
	public void setLeave_repay(BigDecimal leave_repay) {
		this.leave_repay = leave_repay;
	}
	
	
	
	
	
}
